package Sample;

public class IfElseif {

	public static void main(String[] args) {
		int a = 20;
		int b = 55;
		int c = 100;

		if (a == 34) {
			System.out.println("Print Statement_1");
		} else if (b != 55) {
			System.out.println("Print Statement_2");
		} else if (c >= 50) {
			System.out.println("Print Statement_3");
		} else {
			System.out.println("No Statement");
		}

	}

}
